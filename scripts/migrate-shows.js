async function migrate() {
  console.log(`Migrating ${shows.length} shows...`);

  const BATCH_SIZE = 20;
  for (let i = 0; i < shows.length; i += BATCH_SIZE) {
    const batch = shows.slice(i, i + BATCH_SIZE);
    const transaction = client.transaction();

    for (const [date, venue, city, state, notes, ticketUrl] of batch) {
      const address = getAddress(venue, city, state, notes);
      const doc = {
        _type: 'show',
        date: `${date}T20:00:00`,
        venueName: venue,
        venueAddress: address,
        city: city || undefined,
        state: state,
        startTime: '8:00 PM',
      };
      if (notes) doc.notes = notes;
      if (ticketUrl) doc.ticketUrl = ticketUrl;
      transaction.create(doc);
    }

    await transaction.commit();
    console.log(`  Batch ${Math.floor(i / BATCH_SIZE) + 1}: ${batch.length} shows created`);
  }

  console.log('Migration complete!');
}

